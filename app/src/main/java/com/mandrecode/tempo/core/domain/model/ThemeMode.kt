package com.mandrecode.tempo.core.domain.model

/**
 * Represents the theme mode options for the application.
 */
enum class ThemeMode {
    /**
     * Light theme mode
     */
    LIGHT,

    /**
     * Dark theme mode
     */
    DARK,

    /**
     * System theme mode (follows device appearance settings)
     */
    SYSTEM,
}
